func TestSeed(t *testing.T) {
	for _, s := range seedData {
		seed, _ := hex.DecodeString(s[0])
		words, err := ToSeedWords(seed)
		if err != nil {
			t.Fatal(err)
		}
		if words != s[1] {
			fmt.Printf("< %s\n", s[1])
			fmt.Printf("> %s\n", words)
			t.Fatalf("Failed seed: %s", s[0])
		}
	}
}

func TestLookup(t *testing.T) {
	for i := 0; i < 1000; i++ {
		j := rand.Int() % 2048
		if j != lookup(words_en[j]) {
			t.Fatalf("Missed lookup for %d:%d", i, j)
		}
	}
	if lookup("bullshit") != -1 {
		t.Fatal("Found missing word!?")
	}
}

func TestWords(t *testing.T) {
	for _, s := range seedData {
		seed, _ := hex.DecodeString(s[0])
		d, fail := FromSeedWords(s[1])
		if len(fail) > 0 {
			t.Fatalf("failed word: %s", fail)
		}
		if bytes.Compare(seed, d) != 0 {
			t.Fatalf("Failed words: %s", s[1])
		}
	}
}

